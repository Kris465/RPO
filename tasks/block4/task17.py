import math

# Ввод площадей
S_circle = float(input("Введите площадь круга: "))
S_triangle = float(input("Введите площадь равностороннего треугольника: "))

# Вычисляем радиус круга
r = math.sqrt(S_circle / math.pi)

# Вычисляем сторону треугольника
a = math.sqrt((4 * S_triangle) / math.sqrt(3))

# Вписанный радиус треугольника
r_in = (a * math.sqrt(3)) / 6

# Описанный радиус треугольника
R_out = a / math.sqrt(3)

# Проверка а) — уместится ли круг в треугольнике
if r <= r_in:
    print("Круг уместится внутри треугольника.")
else:
    print("Круг не уместится внутри треугольника.")

# Проверка б) — уместится ли треугольник в круге
if r >= R_out:
    print("Треугольник уместится внутри круга.")
else:
    print("Треугольник не уместится внутри круга.")
