n = int(input("Введите число n (10 ≤ n ≤ 999, цифра десятков не равна нулю):"))

# Перебираем возможные цифры a и c
for a in range(1, 10):
    for c in range(0, 10):
        S = n - 10 * a - 10 * c
        if S % 10 == 0:
            b = S // 10
            if 0 <= b <= 9:
                # Проверка условия (цифра десятков числа n не равна нулю)
                tens_digit = (n // 10) % 10
                if tens_digit != 0:
                    x = 100 * a + 10 * b + c
                    print(f"Найденное число x: {x}")
                    break
