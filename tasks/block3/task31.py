# Ввод пользователя
n = int(input("Введите число n (100 ≤ n ≤ 999): "))

# Перебираем все трехзначные числа
for x in range(100, 1000):
    a = x // 100
    b = (x // 10) % 10
    c = x % 10
    
    # Образовательное двузначное число после удаления второй цифры
    two_digit = 10 * a + c
    
    # Новый номер, полученный приписыванием второй цифры справа
    n_candidate = two_digit * 10 + b
    
    # Проверяем, равно ли это введенному числу n
    if n_candidate == n:
        print(f"Исходное число x: {x}")
        break
else:
    print("Не найдено подходящего числа.")
